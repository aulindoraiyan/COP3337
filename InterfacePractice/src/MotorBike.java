public class MotorBike implements Vehicle{
    public void drive(){
        System.out.println("The motorcycle is driving forward");
    }
    public void stop(){
        System.out.println("The motorcycle is stopping");
    }
    public void fuelUp(){
        System.out.println("The motorcycle is fueled up");
    }
    public void reverse(){
        System.out.println("The motorcycle is going reverse");
    }
}
