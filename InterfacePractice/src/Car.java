public class Car implements Vehicle{
    public void drive(){
        System.out.println("The car is moving forward");
    }
    public void stop(){
        System.out.println("The car is stopping");
    }
    public void fuelUp(){
        System.out.println("The car is getting fueled up");
    }
    public void reverse(){
        System.out.println("The car is going reverse");
    }
}
